type LangStore = { lang: Lang; setLang: (l: Lang) => void };

export const useLang = create<LangStore>()(
  persist(
    (set) => ({ lang: "ar", setLang: (lang) => set({ lang }) }),
    { name: "lc-lang" },
  ),
);

export function useT() {
  const lang = useLang((s) => s.lang);
  return { t: dict[lang], lang };
}